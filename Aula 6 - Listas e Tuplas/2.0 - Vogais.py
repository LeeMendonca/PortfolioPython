vogais = ("a", "e", "i", "o", "u")
print(vogais)
print(vogais[0]) # primeiro item - index entre colchetes
print(vogais[1])
print(vogais[2])
print(vogais[3])
print(vogais[4])

print(vogais[-1]) # imprime o último número (pouquíssimo utilizado)