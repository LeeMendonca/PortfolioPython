''' OPERADORES '''

# Operadores de Comparação:
'''
a  ==  b : igual
a  !=  b : diferente
a  <   b : menor
a  >   b : maior
a  <=  b : menor ou igual
a  >=  b : maior ou igual
'''
# Operadores com Portas Lógicas:
'''
a   is   b : são idênticos
a is not b : não são idênticos
a   in   b : a é membro de b
a not in b : a não é membro de b
'''
